// 根据关键词搜索客户端
export function searchClients(keyword) {
  if (!keyword || !keyword.trim()) {
    return mockClients
  }
  
  const filteredClients = mockClients.data.filter(client => 
    client.title.toLowerCase().includes(keyword.toLowerCase()) ||
    client.description.toLowerCase().includes(keyword.toLowerCase())
  )
  
  return {
    ...mockClients,
    data: filteredClients
  }
}